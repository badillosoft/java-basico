    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // 1. Seleccionar el archivo
        JFileChooser fc = new JFileChooser();
        int result = fc.showOpenDialog(this);
        
        if (result == JFileChooser.APPROVE_OPTION) {
            // Agregar el archivo en la lista de archivo
            File f = fc.getSelectedFile();
            
            archivos.add(f);
            
            // Agregar el nombre del archivo a nuestro JList
            model.addElement(f.getAbsolutePath());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // 1. Preguntar donde guardar
        JFileChooser fc = new JFileChooser();
        
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        
        int r = fc.showDialog(this, "Seleccionar Carpeta");
        
        if (r == JFileChooser.APPROVE_OPTION) {
            File f = fc.getSelectedFile();
            this.jTextArea1.setText("Pack to: ");
            this.jTextArea1.append(f.getAbsolutePath() + "\r\n");
            
            try {
                FileWriter fw = new FileWriter(f.getAbsolutePath() + "/backup.pack");
                FileWriter fwm = new FileWriter(f.getAbsolutePath() + "/backup.meta");
                
                for (File fi : archivos) {
                    FileReader fr = new FileReader(fi);
                    
                    int bytes = 0;
                    
                    while (fr.read() != -1) {
                        bytes++;
                    }
                    
                    fr.close();
                    
                    fwm.write(fi.getName() + " " + bytes + "\r\n");
                    
                    this.jTextArea1.append("File: " + fi.getAbsolutePath());
                    this.jTextArea1.append(" bytes: " + bytes + "\r\n");
                    
                    fr = new FileReader(fi);
                    
                    int b;
                    
                    while( (b = fr.read()) != -1) {
                        fw.write(b);
                    }
                    
                    fr.close();
                }
                
                fw.close();
                fwm.close();
            } catch (IOException ex) {
                Logger.getLogger(SuperPacker.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

